package com.netcracker.hack.model;

public class Contact {
    private int id;
    private String phone;
    private String email;
    private String skype;

    public Contact(int id, String phone, String email, String skype) {
        this.id = id;
        this.phone = phone;
        this.email = email;
        this.skype = skype;
    }
}
